#include "../push_swap.h"

int	rb(int *b, int topb)
{
	int	i;
	int	tmp;

	i = topb;
	tmp = b[i];
	while (i > 0)
	{
		b[i] = b[i - 1];
		i--;
	}
	b[i] = tmp;
	write (1, "rb\n", 3);
	return (0);
}
