#include "../push_swap.h"

int	ra(int *a, int topa)
{
	int	tmp;

	tmp = 0;
	tmp = a[topa];
	while (topa > 0)
	{
		a[topa] = a[topa - 1];
		topa--;
	}
	a[0] = tmp;
	write (1, "ra\n", 3);
	return (0);
}
