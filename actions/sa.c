#include "../push_swap.h"

int  sa(int *a, int top)
{   int tmp;

    tmp = 0;
    if(top <= 0)
        return (0);
    else{
        tmp = a[top];
        a[top] = a[top - 1];
        a[top - 1] = tmp; 
    }
   write(1, "sa\n" , 3);
     return (0);
}