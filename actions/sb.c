#include "../push_swap.h"

int	sb(int *b, int top)
{
	int	tmp;

	tmp = 0;
	if (top <= 0)
		return (0);
	else
	{
		tmp = b[top];
		b[top] = b[top - 1];
		b[top - 1] = tmp;
	}
	write(1, "sb\n", 3);
	return (0);
}
